        function ()
            if not client.focus then return end
            local screen = client.focus.screen
            local prev_exists = awful.client.focus.history.get(screen, 1)
            if prev_exists then
                awful.client.focus.history.previous()
            else
                awful.client.focus.byidx(1)
            end
            if client.focus then
                client.focus:raise()
            end
        end),

    -- Standard program
    awful.key({ modkey,           }, "Return", function () awful.util.spawn(terminal) end),
    awful.key({ modkey, "Control" }, "r", awesome.restart),
    awful.key({ modkey, "Control" }, "q", awesome.quit),

    -- Manipulating Conky window
    awful.key({ modkey,           }, "0",
        function ()
                local conky = clnt_table["conky"]
                if conky.hidden then
                    if conky.screen ~= mouse.screen then
                        awful.client.movetoscreen(conky, mouse.screen)
                    end
                    awful.tag.withcurrent(conky)
                    awful.placement.centered(conky)
                elseif not conky:isvisible() then
                    conky:tags(awful.tag.selectedlist(conky.screen))
                    return nil
                else
                    conky:tags({})
                end
                conky.hidden = not conky.hidden
        end),

    -- Managing layout

    awful.key({ modkey,           }, "l",     function () awful.tag.incmwfact( 0.05)    end),
    awful.key({ modkey,           }, "h",     function () awful.tag.incmwfact(-0.05)    end),
